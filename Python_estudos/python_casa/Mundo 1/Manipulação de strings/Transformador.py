frase = "    Olá Mundo Maravilhoso"

print(frase.replace("Maravilhoso", "Feio")) #troca maravilhoso por feio
print(frase.upper()) #deixa tudo maiusculo
print(frase.lower()) #deixa tudo minusculo
print(frase.capitalize()) #deixa apenas a primeira letra maiuscula
print(frase.title()) #deixa a primeira letra de cada palavra maiuscula
print(frase.strip()) #remove espaços inuteis da frase
print(frase.rstrip()) #remove apenas os espaços inuteis da direita
print(frase.lstrip()) #remove apenas os espaços inuteis da esquerda
