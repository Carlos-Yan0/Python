frase = "Olá mundo"
print(frase[4]) #escreve apenas a letra 4
print(frase[4:9]) #escreve até o caractere 8, o nove é ignorado
print(frase[4:9:2]) #pula de dois em dois
print(frase[:4]) #começa do 0 e vai até o 3
print(frase[4:]) #começa do 4 e vai até o final
print(frase[4::2]) #começa do 4, vai até o final e pula de dois em dois
