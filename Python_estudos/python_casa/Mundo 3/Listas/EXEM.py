num = [1, 2, 8, 99, 4]
num.pop()#exclui o ultimo valor da variavel num, posso colocar o espaço da memoria para excluir um espaço desejado
num.append(7) #cria um ultimo valor a varivel e atribui a ela algo
num.insert(3, "oloco")#cria um lugar na posição desejada(3) e coloca algo de minha escolha
num.remove("oloco") #remove algo(apenas o primeiro valor)
num.sort() #organiza as coisas em ordem crescente
num.sort(reverse = True) #organiza as coisas em ordem decrescente
print(num)